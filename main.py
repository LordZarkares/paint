#Black Background and red text.
#Syntax: [color], a "-" before the color name would make it colorize the background.
cprint("[-black][red]Warning!")

#Black Background and red text(RGB).
#Syntax: [R, G, B], a "-" before the R would make it colorize the background.
cprint("[- 40, 40, 40][251, 73, 52]Warning!")

#Some styling too.
#Syntax: [style].
cprint("[-40, 40, 40][red][underlined][bold]Warning!")
